// Compute standings from match results
function computeStandings(){
  let stData={};
  for(let k in GROUPS){stData[k]=GROUPS[k].teams.map(()=>({p:0,w:0,d:0,l:0,gf:0,ga:0,pts:0}))}
  for(let m of ALL_MATCHES){
    if(m.st!=="done"||!m.s)continue;
    let [hS,aS]=m.s.split("-").map(Number);
    let gi=grp(m.h),gi2=grp(m.a);
    if(!gi||!stData[gi])continue;
    let tidx=GROUPS[gi].teams.findIndex(t=>t.includes(m.h));
    let aidx=GROUPS[gi].teams.findIndex(t=>t.includes(m.a));
    if(tidx<0||aidx<0)continue;
    stData[gi][tidx].p++;stData[gi][tidx].gf+=hS;stData[gi][tidx].ga+=aS;
    stData[gi][aidx].p++;stData[gi][aidx].gf+=aS;stData[gi][aidx].ga+=hS;
    if(hS>aS){stData[gi][tidx].w++;stData[gi][tidx].pts+=3;stData[gi][aidx].l++;}
    else if(hS<aS){stData[gi][aidx].w++;stData[gi][aidx].pts+=3;stData[gi][tidx].l++;}
    else{stData[gi][tidx].d++;stData[gi][aidx].d++;stData[gi][tidx].pts++;stData[gi][aidx].pts++;}
  }
  // Sort each group by pts, then GD, then GF
  for(let k in stData){
    let arr=stData[k].map((d,i)=>({...d,idx:i}));
    arr.sort((a,b)=>{
      if(b.pts!==a.pts)return b.pts-a.pts;
      let gdA=a.gf-a.ga,gdB=b.gf-b.ga;
      if(gdB!==gdA)return gdB-gdA;
      return b.gf-a.gf;
    });
    stData[k]=arr;
  }
  return stData;
}

