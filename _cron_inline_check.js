// Flag emoji → flagcdn image URL
function fl2url(e){
  if(!e)return null;
  let cp=[...e].map(c=>c.codePointAt(0)).filter(c=>c>=0x1F1E6&&c<=0x1F1FF);
  if(cp.length<2)return null;
  let cc=String.fromCharCode(cp[0]-0x1F1E6+65,cp[1]-0x1F1E6+65).toLowerCase();
  return 'https://flagcdn.com/24x18/'+cc+'.png';
}
function flagHtml(e){let u=fl2url(e);return u?'<img src="'+u+'" alt="" loading="lazy">':e;}

// Team display: map short name → {fl, nm} from GROUPS
function td(n){
  // Check TM alias mapping first. teamMeta() returns {flag,name}, so normalize it to td()'s {fl,nm} contract.
  for(let [full,alias] of Object.entries(TM)){
    if(alias===n){let m=teamMeta(full);return{fl:flagHtml(m.flag),nm:m.name};}
  }
  for(let g of Object.values(GROUPS)){
    for(let t of g.teams){
      let nn=stripFlag(t);
      if(nn===n)return{fl:flagHtml(t.slice(0,t.length-nn.length)),nm:nn};
    }
  }
  return{fl:'',nm:n};
}
// Parse match time string → Date (Beijing time, current year)
function parseMatchTime(s){
  let [md,tm]=s.split(' ');
  let [m,d]=md.split('/').map(Number);
  let [h,mi]=tm.split(':').map(Number);
  return new Date(2026,m-1,d,h,mi,0);
}
