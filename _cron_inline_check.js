// Check if a match is currently in progress
function isLive(m){
  if(m.st==="live")return true;
  if(m.st!=="upcoming"||!m.t)return false;
  let now=new Date();
  let dt=parseMatchTime(m.t);
  let end=dt.getTime()+2.5*60*60*1000;
  return dt<=now&&now<=end;
}
